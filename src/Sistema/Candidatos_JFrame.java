    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        nombrepartido = jTextField1.getText();
        nombre = jTextField2.getText();
        apellido = jTextField3.getText();
        

        try {
        dni = Integer.parseInt(jTextField4.getText().trim());
        } catch (NumberFormatException e) {
                javax.swing.JOptionPane.showMessageDialog(null, "DNI inválido.");
            return;
        }
        
        PartidoPolitico encontrado = null;
        for (int i=0; i<sistema.p_partidos; i++) {
            if (sistema.partidos[i] != null && sistema.partidos[i].getNombre().equalsIgnoreCase(nombrepartido)) {
                encontrado = sistema.partidos[i];
                break;
            }
        }
        
        if (encontrado == null) {
            javax.swing.JOptionPane.showMessageDialog(null, "El partido ingresado no existe.");
        }
        else{
            partido = encontrado;
        
            Candidato c = new Candidato(partido, nombre, apellido, dni);
            sistema.IngresarCandidato(c);
            Cargartabla();
        

        }
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

