    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        tipo = this.jTextField1.getText();
        nombre = this.jTextField2.getText();
        apellido = this.jTextField3.getText();
        
        try {
        dni = Integer.parseInt(jTextField4.getText().trim());
        } catch (NumberFormatException e) {
                javax.swing.JOptionPane.showMessageDialog(null, "DNI inv√°lido.");
            return;
        }
        
        if(!tipo.equalsIgnoreCase("presidente") && !tipo.equalsIgnoreCase("vocal") && !tipo.equalsIgnoreCase("secretario")){
            javax.swing.JOptionPane.showMessageDialog(null, "Tipo de miembro de mesa invalido.");
        }
        else{
            MiembrosMesa m = new MiembrosMesa(tipo, nombre, apellido, dni);
            sistema.IngresarMiembro(m);
            Cargartabla();
        }
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

